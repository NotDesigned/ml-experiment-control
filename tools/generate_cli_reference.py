#!/usr/bin/env python3
"""Generate deterministic Markdown help for the packaged sanitizer CLI."""

from __future__ import annotations

import os
import subprocess
import sys
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
TARGET = ROOT / "docs" / "cli_reference.md"


def render() -> str:
    executable = os.environ.get("EXPERIMENTCTL_SAFE_SCO_BIN", "experiment-safe-sco")
    result = subprocess.run(
        [executable, "--help"], text=True, capture_output=True, check=False,
    )
    if result.returncode != 0:
        raise RuntimeError("Rust sanitizer help command failed")
    help_text = result.stdout.rstrip()
    return f"""# CLI Reference

<!-- Generated by tools/generate_cli_reference.py; do not edit manually. -->

`experiment-safe-sco` is the package's deliberately narrow sanitizer CLI. It
does not submit, cancel, or otherwise control experiments; controllers consume
the Python backend API for those operations.

```text
{help_text}
```
"""


def main(argv: list[str] | None = None) -> int:
    arguments = list(argv or sys.argv[1:])
    expected = render()
    if arguments == ["--check"]:
        actual = TARGET.read_text(encoding="utf-8") if TARGET.is_file() else ""
        if actual != expected:
            print(
                "CLI reference is stale; run tools/generate_cli_reference.py",
                file=sys.stderr,
            )
            return 1
        return 0
    if arguments:
        print("usage: generate_cli_reference.py [--check]", file=sys.stderr)
        return 2
    TARGET.parent.mkdir(parents=True, exist_ok=True)
    TARGET.write_text(expected, encoding="utf-8")
    print(TARGET.relative_to(ROOT))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
